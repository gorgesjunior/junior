
public class Proventos {

	private int id;
	private int ano;
	private int mes;
	private Integer valor;
	private Integer imposto;
	private int pessoas_id;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public int getAno() {
		return ano;
	}

	public void setAno(int ano) {
		this.ano = ano;
	}

	public int getMes() {
		return mes;
	}

	public void setMes(int mes) {
		this.mes = mes;
	}

	public Integer getValor() {
		return valor;
	}

	public void setValor(Integer valor) {
		this.valor = valor;
	}

	public Integer getImposto() {
		return imposto;
	}

	public void setImposto(Integer imposto) {
		this.imposto = imposto;
	}

	public int getPessoas_id() {
		return pessoas_id;
	}

	public void setPessoas_id(int pessoas_id) {
		this.pessoas_id = pessoas_id;
	}

}
